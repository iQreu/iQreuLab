(function () {
  const KEY = "iqt-lang";
  const cachePL = {};   // key -> original Polish textContent
  const cachePLph = {}; // key -> original Polish placeholder

  function apply(lang) {
    const toEN = lang === "en";
    document.documentElement.setAttribute("lang", lang);

    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const k = el.getAttribute("data-i18n");
      if (!(k in cachePL)) cachePL[k] = el.textContent;
      if (toEN) { if (EN[k] != null) el.textContent = EN[k]; }
      else el.textContent = cachePL[k];
    });

    document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
      const k = el.getAttribute("data-i18n-ph");
      if (!(k in cachePLph)) cachePLph[k] = el.getAttribute("placeholder") || "";
      if (toEN) { if (EN[k] != null) el.setAttribute("placeholder", EN[k]); }
      else el.setAttribute("placeholder", cachePLph[k]);
    });

    document.querySelectorAll("[data-lang-label]").forEach((el) => {
      el.innerHTML = toEN ? 'EN <span style="opacity:.45">/ PL</span>' : 'PL <span style="opacity:.45">/ EN</span>';
    });
    try { localStorage.setItem(KEY, lang); } catch (e) {}
    window.__lang = lang;
  }

  window.iqtLang = {
    get: () => window.__lang || "pl",
    set: apply,
    toggle: () => apply((window.__lang || "pl") === "pl" ? "en" : "pl")
  };

  document.addEventListener("DOMContentLoaded", () => {
    let saved = "pl";
    try { saved = localStorage.getItem(KEY) || "pl"; } catch (e) {}
    apply(saved);
  });
})();
